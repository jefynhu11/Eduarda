package aula4;

public class Atividade1 {

	public static void main(String[] args) {
		
		int [][] m = new int [10][10];
		int zero = 0;
		
		for (int i = 0; i < m.length; i++) {
			System.out.println();			
			for (int j = 0; j < m.length; j++) {
				System.out.print(m[zero][zero] + " ");
			}
		}
		
	}

}
